package ch05;

public class PrimitiveType {
    void main() {
        int age = 25;
        System.out.println(age);

        age = 30;
        System.out.println(age);

        int score = 100;
        System.out.println(score);

        byte b = 125;
        System.out.println(b);

        int num = 1000000000;
        System.out.println(num);

        double pi = 3.141592;
        System.out.println(pi);
    }
}
